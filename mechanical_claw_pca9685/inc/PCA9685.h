/**
  ******************************************************************************
  * @file    pca9685.h
  * @author  mzx
  * @brief   PCA9685 16通道PWM驱动器接口（用于舵机控制）
  * @version 1.0
  * @date    2026-06-23
  ******************************************************************************
  * @attention
  * - 本模块封装了 PCA9685 的 I2C 通信及寄存器操作。
  * - 默认 PWM 频率 50Hz，输出模式为推挽。
  * - 机械爪的最小闭合角度为 80°。
  ******************************************************************************
  */

/* Define to prevent recursive inclusion -------------------------------------*/
#ifndef __PCA9685_H
#define __PCA9685_H

/* Includes ------------------------------------------------------------------*/
#include "main.h"

/**
 * @brief  初始化 PCA9685 模块
 * @note   配置为 50Hz 推挽输出，直接驱动舵机。
 *         需要在系统启动时调用一次，且先于任何舵机控制指令。
 */
void PCA9685_Init(void);

/**
 * @brief  控制指定通道的舵机转动到目标角度
 * @param  ch    舵机通道号，范围 0~15（超出自动钳位为 15）
 * @param  angle 目标角度，范围 0~180°（超出自动钳位为 180°）
 * @note   角度与脉宽线性映射：0°对应 0.5ms，180°对应 2.5ms。
 *         机械爪应用时，最小有效闭合角度为 80°，低于此值可能导致无法闭合。
 */
void Servo_SetAngle(uint8_t ch, uint8_t angle);


#endif /* __PCA9685_H */
